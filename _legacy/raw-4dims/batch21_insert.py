def insert_with_retry(conn, signals):
    cursor = conn.cursor()
    max_retries = 3
    inserted = 0
    for sig in signals:
        for attempt in range(max_retries):
            try:
                cursor.execute(
                    """INSERT INTO industry_signals 
                       (date, category, keyword, target, signal_content, confidence, status)
                       VALUES (?, ?, ?, ?, ?, ?, ?)""",
                    sig
                )
                inserted += 1
                break
            except sqlite3.OperationalError as e:
                if "locked" in str(e).lower() and attempt < max_retries - 1:
                    time.sleep(1)
                    continue
                else:
                    logger.info(f"❌ 插入失败: {sig[0]} {sig[1]} {sig[2]} — {e}")
                    break
    conn.commit()
    return inserted

