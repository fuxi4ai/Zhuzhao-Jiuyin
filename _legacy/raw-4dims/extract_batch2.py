def insert_signals():
    """插入信号到数据库，含database locked重试"""
    conn = None
    for attempt in range(4):  # 最多重试3次+首次
        try:
            conn = sqlite3.connect(DB_PATH, timeout=10)
            cursor = conn.cursor()
            
            count = 0
            for sig in signals:
                date, category, keyword, target, signal_content, confidence = sig
                created_at = datetime.now().strftime("%Y-%m-%d %H:%M:%S")
                cursor.execute(
                    """INSERT INTO industry_signals 
                       (date, category, keyword, target, signal_content, confidence, status, created_at)
                       VALUES (?, ?, ?, ?, ?, ?, 'new', ?)""",
                    (date, category, keyword, target, signal_content, confidence, created_at)
                )
                count += 1
            
            conn.commit()
            logger.info(f"✅ 导读批次2完成：5文件 {count}信号")
            return count
            
        except sqlite3.OperationalError as e:
            if "locked" in str(e).lower() and attempt < 3:
                time.sleep(1)
                continue
            raise
        finally:
            if conn:
                conn.close()

if __name__ == "__main__":
    insert_signals()
