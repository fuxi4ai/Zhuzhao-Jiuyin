# ============ Write to DB ============
def insert_with_retry(conn, sql, params, max_retries=3):
    for i in range(max_retries):
        try:
            conn.execute(sql, params)
            return True
        except sqlite3.OperationalError as e:
            if "locked" in str(e).lower() and i < max_retries - 1:
                time.sleep(1)
            else:
                raise
    return False

def main():
    conn = sqlite3.connect(DB)
    cursor = conn.cursor()

    sql = """INSERT INTO industry_signals
             (date, category, keyword, target, signal_content, confidence, status)
             VALUES (?, ?, ?, ?, ?, ?, 'new')"""

    count = 0
    for sig in signals:
        if insert_with_retry(conn, sql, sig):
            count += 1

    conn.commit()
    conn.close()
    logger.info(f"✅ 批次11完成：5文件 {count}信号")

if __name__ == "__main__":
    main()
