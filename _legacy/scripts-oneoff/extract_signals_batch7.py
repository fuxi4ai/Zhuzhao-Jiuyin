def insert_signals(signals, db_path):
    """写入信号，database locked时重试3次"""
    conn = sqlite3.connect(db_path)
    cursor = conn.cursor()
    
    created_at = datetime.now().strftime("%Y-%m-%d %H:%M:%S")
    
    inserted = 0
    for sig in signals:
        date, category, keyword, target, content, confidence = sig
        retry = 0
        while retry < 3:
            try:
                cursor.execute(
                    """INSERT INTO industry_signals 
                       (date, category, keyword, target, signal_content, confidence, status, created_at)
                       VALUES (?, ?, ?, ?, ?, ?, 'new', ?)""",
                    (date, category, keyword, target, content, confidence, created_at)
                )
                conn.commit()
                inserted += 1
                break
            except sqlite3.OperationalError as e:
                if "locked" in str(e).lower():
                    retry += 1
                    time.sleep(1)
                else:
                    raise
        else:
            print(f"⚠️ 插入失败(重试3次): {category} - {keyword}")
    
    conn.close()
    return inserted

