def write_signals(signals, db_path=DB_PATH):
    conn = sqlite3.connect(db_path, timeout=30)
    cursor = conn.cursor()
    # Ensure table exists
    cursor.execute("""
        CREATE TABLE IF NOT EXISTS industry_signals (
            id INTEGER PRIMARY KEY,
            date TEXT NOT NULL,
            category TEXT NOT NULL,
            keyword TEXT,
            target TEXT,
            signal_content TEXT,
            confidence TEXT DEFAULT 'P2',
            status TEXT DEFAULT 'new',
            created_at TEXT
        )
    """)
    conn.commit()

    # Get max id
    cursor.execute("SELECT COALESCE(MAX(id), 0) FROM industry_signals")
    max_id = cursor.fetchone()[0]

    inserted = 0
    for sig in signals:
        max_id += 1
        date, category, keyword, target, logic, detail, confidence, status = sig
        signal_content = logic + detail
        created_at = datetime.now().strftime("%Y-%m-%d %H:%M:%S")

        # Retry logic for database locked
        for attempt in range(3):
            try:
                cursor.execute(
                    """INSERT INTO industry_signals
                       (id, date, category, keyword, target, signal_content, confidence, status, created_at)
                       VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?)""",
                    (max_id, date, category, keyword, target, signal_content, confidence, status, created_at)
                )
                conn.commit()
                inserted += 1
                print(f"  ✅ [{date}] {category} | {keyword} | {confidence} | 目标: {target or '无'}")
                break
            except sqlite3.OperationalError as e:
                if "locked" in str(e).lower() and attempt < 2:
                    time.sleep(1)
                else:
                    raise

    conn.close()
    return inserted


