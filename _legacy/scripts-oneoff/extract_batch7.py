def insert_with_retry(conn, sql, data, max_retries=3):
    for attempt in range(max_retries):
        try:
            conn.execute(sql, data)
            return True
        except sqlite3.OperationalError as e:
            if "locked" in str(e).lower() and attempt < max_retries - 1:
                time.sleep(1)
                continue
            raise
    return False

def main():
    conn = sqlite3.connect(DB_PATH)
    cursor = conn.cursor()

    sql = """INSERT OR IGNORE INTO industry_signals 
             (date, category, keyword, target, signal_content, confidence, status, created_at)
             VALUES (?, ?, ?, ?, ?, ?, 'new', ?)"""

    count = 0
    for sig in signals:
        date, cat, kw, tgt, content, conf = sig
        now = datetime.now().strftime("%Y-%m-%d %H:%M:%S")
        if insert_with_retry(conn, sql, (date, cat, kw, tgt, content, conf, now)):
            count += 1

    conn.commit()
    conn.close()
    print(f"✅ 导读批次7完成：5文件 {count}信号")

if __name__ == "__main__":
    main()
