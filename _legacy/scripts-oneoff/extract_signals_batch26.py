def insert_with_retry(conn, signals, max_retries=3):
    """插入信号，遇database locked重试"""
    cursor = conn.cursor()
    inserted = 0
    for sig in signals:
        for attempt in range(max_retries):
            try:
                cursor.execute("""
                    INSERT INTO industry_signals 
                    (date, category, keyword, target, signal_content, confidence, status, created_at)
                    VALUES (?, ?, ?, ?, ?, ?, ?, datetime('now', '+8 hours'))
                """, sig)
                inserted += 1
                break
            except sqlite3.OperationalError as e:
                if "locked" in str(e) and attempt < max_retries - 1:
                    time.sleep(1)
                else:
                    print(f"❌ 插入失败: {sig[4][:50]}... 错误: {e}")
                    break
    conn.commit()
    return inserted


def main():
    os.makedirs(os.path.dirname(DB_PATH), exist_ok=True)
    
    conn = sqlite3.connect(DB_PATH)
    cursor = conn.cursor()
    
    # 确保表存在
    cursor.execute("""
        CREATE TABLE IF NOT EXISTS industry_signals (
            id INTEGER PRIMARY KEY AUTOINCREMENT,
            date TEXT NOT NULL,
            category TEXT NOT NULL,
            keyword TEXT,
            target TEXT,
            signal_content TEXT,
            confidence TEXT DEFAULT 'P2',
            status TEXT DEFAULT 'new',
            created_at TEXT
        )
    """)
    conn.commit()
    
    total = len(signals)
    inserted = insert_with_retry(conn, signals)
    
    # 统计
    cursor.execute("SELECT COUNT(*) FROM industry_signals")
