NEWCOLS = [("position_pct_min","REAL"),("position_pct_max","REAL"),("position_repr","REAL"),
           ("position_stance","TEXT"),("position_conf","TEXT"),("position_raw","TEXT"),
           ("position_source","TEXT")]

def main():
    ap=argparse.ArgumentParser(); ap.add_argument("--commit",action="store_true"); a=ap.parse_args()
    bundle={b["date"]:b for b in json.load(open(BUNDLE,encoding="utf-8"))}
    c=config.connect_write(DB); cur=c.cursor()
    existing={r[1] for r in cur.execute("PRAGMA table_info(dim4_trade_plan)")}
    for col,typ in NEWCOLS:
        if col not in existing:
            print(f"[schema] ADD COLUMN {col} {typ}")
            if a.commit: cur.execute(f"ALTER TABLE dim4_trade_plan ADD COLUMN {col} {typ}")
    have={r[0] for r in cur.execute("select date from dim4_trade_plan")}
    ins=upd=0
    for d in sorted(J):
        mn,mx,rp,st,cf=J[d]
        b=bundle.get(d,{})
        raw=" / ".join(b.get("top",[])[:2])[:300]
        src=b.get("source","")
        if d not in have:
            ins+=1
            if a.commit:
                cur.execute("INSERT INTO dim4_trade_plan(date,position_pct_min,position_pct_max,position_repr,position_stance,position_conf,position_raw,position_source) VALUES(?,?,?,?,?,?,?,?)",
                            (d,mn,mx,rp,st,cf,raw,src))
        else:
            upd+=1
            if a.commit:
                cur.execute("UPDATE dim4_trade_plan SET position_pct_min=?,position_pct_max=?,position_repr=?,position_stance=?,position_conf=?,position_raw=?,position_source=? WHERE date=?",
                            (mn,mx,rp,st,cf,raw,src,d))
    if a.commit: c.commit()
    print(f"判读天数 {len(J)} | 新增行 {ins} | 更新行 {upd} | {'已落盘' if a.commit else 'DRY-RUN(未写)'}")
    # 分布
    from collections import Counter
    print("立场分布:",dict(Counter(v[3] for v in J.values())))
    print("置信分布:",dict(Counter(v[4] for v in J.values())))

if __name__=="__main__": main()
